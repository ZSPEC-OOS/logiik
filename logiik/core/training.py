# ─── Curriculum Dataset ───────────────────────────────────────────────────────

class CurriculumDataset(Dataset):
    """
    Dataset implementing the Q+A training structure.
    Migrated and renamed from legacy CurriculumDataset.
    Preserves all logic including train/val split and
    soft teacher logit simulation.
    """

    def __init__(
        self,
        examples: List[TrainingExample],
        tokenizer,
        max_length: int = 512,
        generative_ratio: float = 0.3,
        val_ratio: float = 0.1,
        _items=None,
    ):
        self.tokenizer = tokenizer
        self.max_length = max_length
        self.generative_ratio = generative_ratio

        if _items is not None:
            self.items = _items
            self.examples = examples
            self._val_dataset = None
            return

        all_items = [
            (ex, ans_idx)
            for ex in examples
            for ans_idx in range(len(ex.answers))
        ]
        split = int(len(all_items) * (1 - val_ratio))
        self.items = all_items[:split]
        val_items = all_items[split:]
        self._val_dataset = CurriculumDataset(
            examples, tokenizer, max_length,
            generative_ratio, _items=val_items
        )
        self.examples = examples

    @property
    def val_dataset(self):
        return self._val_dataset

    def __len__(self):
        return len(self.items)

    def __getitem__(self, idx) -> ProcessedExample:
        example, ans_idx = self.items[idx]
        question_prefix = f"Question: {example.question}\nAnswer:"
        formatted_text = (
            f"Question: {example.question}\n"
            f"Answer: {example.answers[ans_idx]}"
        )
        encoding = self.tokenizer(
            formatted_text,
            max_length=self.max_length,
            padding="max_length",
            truncation=True,
            return_tensors="pt"
        )
        prefix_ids = self.tokenizer(
            question_prefix,
            add_special_tokens=False,
            return_tensors="pt"
        )["input_ids"]
        prefix_len = prefix_ids.shape[1]
        labels = encoding["input_ids"].clone()
        labels[labels == self.tokenizer.pad_token_id] = -100
        labels[:, :prefix_len] = -100

        teacher_logits = self._simulate_teacher_logits(example, ans_idx)
        weight = 1.0 + example.difficulty

        return ProcessedExample(
            input_ids=encoding["input_ids"].squeeze(0),
            attention_mask=encoding["attention_mask"].squeeze(0),
            labels=labels.squeeze(0),
            teacher_logits=teacher_logits,
            weight=weight
        )

    def _simulate_teacher_logits(
        self, example: TrainingExample, ans_idx: int
    ) -> torch.Tensor:
        vocab_size = self.tokenizer.vocab_size
        answer_tokens = self.tokenizer.encode(
            example.answers[ans_idx], add_special_tokens=False
        )
        seq_len = len(answer_tokens)
        logits = torch.zeros(seq_len, vocab_size)
        for pos, token_id in enumerate(answer_tokens):
            if token_id < vocab_size:
                logits[pos, token_id] = 2.0
        return logits


# ─── Generative Curriculum ────────────────────────────────────────────────────

class GenerativeCurriculum:
    """
    Logiik curriculum engine.
    Migrated from cognita/training/curriculum.py GenerativeCurriculum.

    Phase 7 updated: "Drosophila AI Framework" replaced with
    "Niche Scientific Reasoning" (teacher_student=True,
    correctness_threshold=0.90).

    Phase advancement uses plateau detection from server.py
    (val_loss plateau) combined with coverage + saturation
    metrics added in CHUNK-08.
    """

    def __init__(
        self,
        teacher_orchestrator,
        tokenizer,
        topics_description: str = "",
        phase_ratios: Tuple[float, ...] = (
            0.20, 0.20, 0.15, 0.15, 0.12, 0.10, 0.08
        ),
        phase_topics: Optional[Dict[str, List[str]]] = None,
        topics_per_session: int = 5,
    ):
        self.teacher = teacher_orchestrator
        self.tokenizer = tokenizer
        self.topics_description = topics_description
        self.topics_per_session = topics_per_session
        self.current_phase = 0   # 0-indexed internally

        # Phase names from canonical PHASES list
        self.phase_names = [p.display_name for p in PHASES]
        self.phase_configs = {p.display_name: p for p in PHASES}

        self.phase_ratios = self._normalize_ratios(phase_ratios)
        self.phase_topics: Dict[str, List[str]] = phase_topics or {}
        self._topic_cursors: Dict[str, int] = {
            name.lower(): 0 for name in self.phase_names
        }

        # Phase 7 teacher-student loop instance
        self._phase7_loop = Phase7TeacherStudentLoop()

        logger.info(
            f"GenerativeCurriculum initialised: "
            f"{len(self.phase_names)} phases"
        )

    def _normalize_ratios(
        self, ratios: Tuple[float, ...]
    ) -> Tuple[float, ...]:
        n = len(self.phase_names)
        if not ratios:
            return tuple(0.3 for _ in range(n))
        if len(ratios) < n:
            pad = ratios[-1]
            return tuple(ratios) + tuple(pad for _ in range(n - len(ratios)))
        return tuple(ratios[:n])

    def _get_topics(self) -> List[str]:
        phase_key = self.phase_names[self.current_phase].lower()
        topics = self.phase_topics.get(phase_key, [])
        if not topics:
            return (
                [self.topics_description]
                if self.topics_description.strip()
                else ["general_knowledge"]
            )
        n = len(topics)
        start = self._topic_cursors[phase_key]
        selected = [
            topics[(start + i) % n]
            for i in range(self.topics_per_session)
        ]
        self._topic_cursors[phase_key] = (
            (start + self.topics_per_session) % n
        )
        return selected

    def generate_phase_batch(
        self,
        batch_size: int,
        question_bank=None,
    ) -> CurriculumDataset:
        """Generate training batch for current phase."""
        phase_name = self.phase_names[self.current_phase]
        phase_config = self.phase_configs[phase_name]

        logger.info(
            f"Generating batch: phase={phase_name}, "
            f"batch_size={batch_size}"
        )

        # Route to phase-specific generator
        if self.current_phase == 0:
            examples = self._gen_memorization(batch_size)
        elif self.current_phase == 1:
            examples = self._gen_generation(batch_size)
        elif self.current_phase == 2:
            examples = self._gen_abstraction(batch_size)
        elif self.current_phase == 3:
            examples = self._gen_engineering(batch_size)
        elif self.current_phase == 4:
            examples = self._gen_coding(batch_size)
        elif self.current_phase == 5:
            examples = self._gen_scientific_reasoning(batch_size)
        elif self.current_phase == 6:
            # Phase 7 (0-indexed: 6) — Niche Scientific Reasoning
            examples = self._gen_niche_scientific(batch_size)
        else:
            examples = self._gen_memorization(batch_size)

        # Deduplication via question bank
        if question_bank and examples:
            filtered = [
                ex for ex in examples
                if question_bank.check_and_log(
                    ex.question, self.topics_description
                )
            ]
            examples = filtered if filtered else examples[:1]

        return CurriculumDataset(
            examples,
            self.tokenizer,
            generative_ratio=self.phase_ratios[self.current_phase]
        )

    def advance_phase(self) -> bool:
        """Advance to next phase. Returns False if already at last phase."""
        if self.current_phase < len(self.phase_names) - 1:
            self.current_phase += 1
            logger.info(
                f"Advanced to phase {self.current_phase + 1}: "
                f"{self.phase_names[self.current_phase]}"
            )
            return True
        logger.info("All phases complete.")
        return False

    # ─── Phase generators ─────────────────────────────────────────────────

    def _make_example(
        self,
        topic: str,
        prefix: str,
        suffix: str,
        difficulty: float,
        domain_prefix: str,
    ) -> TrainingExample:
        """Helper: generate one example via teacher, apply prefix/suffix."""
        base = self.teacher.teachers[0].generate_training_example(
            topic, difficulty=difficulty, num_answers=5
        )
        return TrainingExample(
            question=f"{prefix}{base.question}\n{suffix}",
            answers=base.answers,
            correct_indices=base.correct_indices,
            difficulty=difficulty,
            domain=f"{domain_prefix}_{base.domain}",
            explanation=base.explanation,
        )

    def _gen_memorization(self, count: int) -> List[TrainingExample]:
        topics = self._get_topics()
        return self.teacher.generate_curriculum_batch(
            topics,
            examples_per_topic=max(1, count // len(topics)),
            difficulty_progression=True
        )

    def _gen_generation(self, count: int) -> List[TrainingExample]:
        topics = self._get_topics()
        examples = []
        for topic in topics:
            base = self.teacher.teachers[0].generate_training_example(
                topic, num_answers=3
            )
            examples.append(TrainingExample(
                question=(
                    base.question +
                    "\n(Provide an original answer based on "
                    "your understanding)"
                ),
                answers=["[GENERATE]"],
                correct_indices=[0],
                difficulty=min(base.difficulty * 1.2, 1.0),
                domain=base.domain,
                explanation="Generate original answer showing understanding",
            ))
        return examples[:count]

    def _gen_abstraction(self, count: int) -> List[TrainingExample]:
        topics = self._get_topics()
        return [
            self._make_example(
                topics[i % len(topics)],
                prefix="Connecting ideas across fields: ",
                suffix="(Draw on knowledge from multiple domains)",
                difficulty=0.90,
                domain_prefix="cross_domain",
            )
            for i in range(count)
        ]

    def _gen_engineering(self, count: int) -> List[TrainingExample]:
        topics = self._get_topics()
        return [
            self._make_example(
                topics[i % len(topics)],
                prefix="Engineering execution scenario: ",
                suffix=(
                    "(Plan implementation, propose tests, debug likely "
                    "failure modes, and justify trade-offs in "
                    "reliability/security/performance)"
                ),
                difficulty=0.92,
                domain_prefix="engineering_execution",
            )
            for i in range(count)
        ]

    def _gen_coding(self, count: int) -> List[TrainingExample]:
        topics = self._get_topics()
        return [
            self._make_example(
                topics[i % len(topics)],
                prefix="Coding mastery challenge: ",
                suffix=(
                    "(Demonstrate robust implementation choices across "
                    "common programming languages)"
                ),
                difficulty=0.95,
                domain_prefix="coding_mastery",
            )
            for i in range(count)
        ]

    def _gen_scientific_reasoning(self, count: int) -> List[TrainingExample]:
        topics = self._get_topics()
        return [
            self._make_example(
                topics[i % len(topics)],
                prefix="Scientific reasoning challenge: ",
                suffix=(
                    "(State hypotheses, define controls, identify "
                    "confounders, and provide uncertainty-aware conclusions)"
                ),
                difficulty=0.96,
                domain_prefix="scientific_reasoning",
            )
            for i in range(count)
        ]

    def _gen_niche_scientific(self, count: int) -> List[TrainingExample]:
        """
        Phase 7: Niche Scientific Reasoning.
        Generates interdisciplinary, mechanistic, and hypothetical
        scientific prompts requiring stepwise reasoning.

        NOTE: In full Phase 7 operation, these examples feed into
        Phase7TeacherStudentLoop.run_feedback_loop() for iterative
        correctness enforcement. The CurriculumDataset returned here
        is used for the base training pass; the feedback loop runs
        as a separate process via the API.
        """
        topics = self._get_topics()
        question_types = [
            "hypothetical_scenario",
            "multi_step_experimental_design",
            "interdisciplinary_synthesis",
            "mechanistic_reasoning",
            "rare_data_interpretation",
        ]
        examples = []
        for i in range(count):
            topic = topics[i % len(topics)]
            q_type = question_types[i % len(question_types)]
            base = self.teacher.teachers[0].generate_training_example(
                topic, difficulty=0.95, num_answers=5
            )
            examples.append(TrainingExample(
                question=(
                    f"[{q_type.replace('_', ' ').title()}] "
                    f"{base.question}\n"
                    "(Provide stepwise reasoning: identify mechanism, "
                    "predict outcome, explain experimental evidence, "
                    "address edge cases and interdisciplinary connections.)"
                ),
                answers=base.answers,
                correct_indices=base.correct_indices,
                difficulty=0.98,
                domain=f"niche_scientific_{base.domain}",
                explanation=(
                    f"Niche scientific reasoning [{q_type}]: "
                    f"{base.explanation}"
                ),
            ))
        return examples

    @property
    def phase7_loop(self) -> Phase7TeacherStudentLoop:
        """Access Phase 7 teacher-student loop directly."""
        return self._phase7_loop


# ─── Phase Completion Monitor ─────────────────────────────────────────────────

class PhaseCompletionMonitor:
    """
    Objective, data-driven phase completion monitor.

    Combines two signals to determine when a curriculum phase is complete:
      1. Prompt coverage ratio  — breadth: what fraction of phase prompts
                                  have received valid student answers.
      2. Semantic saturation    — depth: are student outputs becoming
                                  semantically redundant (high similarity
                                  to recent outputs = knowledge plateau).

    Both signals must exceed their configured thresholds simultaneously
    before a phase is declared complete.

    This runs alongside the existing validation-loss plateau detection
    in the training loop (server.py). Phase advancement requires EITHER:
      - Plateau detection triggers (val_loss stagnant for N checks), OR
      - PhaseCompletionMonitor declares complete (coverage + saturation).
    Whichever occurs first advances the phase.

    Usage:
        monitor = PhaseCompletionMonitor(phase_config, phase_prompts)
        # After each student answer:
        monitor.update(answer_text, answer_embedding)
        if monitor.is_complete():
            curriculum.advance_phase()
        metrics = monitor.get_metrics()
    """

    def __init__(
        self,
        phase_config: PhaseConfig,
        phase_prompts: List[Dict],
        thresholds: Optional[Dict] = None,
    ):
        """
        Args:
            phase_config:   PhaseConfig for the current phase.
            phase_prompts:  List of prompt dicts, each must contain
                            a unique 'id' field.
                            e.g. [{'id': 'p1', 'text': '...'}, ...]
            thresholds:     Override default thresholds. Keys:
                            'coverage_ratio', 'saturation_score',
                            'max_iterations'.
        """
        self._phase_config = phase_config
        self._phase_prompts = phase_prompts
        self._total_prompts = max(len(phase_prompts), 1)

        # Merge thresholds: phase_config.completion_criteria
        # → config.yaml phase_monitor → caller override
        from logiik.config import CONFIG
        cfg_thresholds = CONFIG.get("phase_monitor", {})
        phase_thresholds = phase_config.completion_criteria or {}

        self._thresholds = {
            "coverage_ratio": phase_thresholds.get(
                "coverage_ratio",
                cfg_thresholds.get("coverage_ratio", 0.95)
            ),
            "saturation_score": phase_thresholds.get(
                "saturation_score",
                cfg_thresholds.get("saturation_score", 0.90)
            ),
            "max_iterations": phase_thresholds.get(
                "max_iterations",
                cfg_thresholds.get("max_iterations", 1000)
            ),
        }
        if thresholds:
            self._thresholds.update(thresholds)

        # State
        self._covered_prompts: set = set()
        self._recent_embeddings: List[np.ndarray] = []
        self._past_embeddings: List[np.ndarray] = []
        self._iteration: int = 0
        self._metrics_log: List[Dict] = []

        logger.info(
            f"PhaseCompletionMonitor initialised: "
            f"phase={phase_config.display_name}, "
            f"total_prompts={self._total_prompts}, "
            f"thresholds={self._thresholds}"
        )

    # ─── Public API ───────────────────────────────────────────────────────

    def update(
        self,
        answer_text: str,
        answer_embedding: np.ndarray,
        prompt_id: Optional[str] = None,
    ) -> Dict:
        """
        Process one student answer.

        Call this after every student output during training.

        Args:
            answer_text:      Raw string output from student model.
            answer_embedding: SPECTER2 embedding of answer_text,
                              shape (768,).
            prompt_id:        ID of the prompt this answer responds to.
                              If None, coverage tracking is skipped for
                              this answer (saturation still tracked).

        Returns:
            Dict with current coverage_ratio, saturation_score,
            iteration, and is_complete flag.
        """
        self._iteration += 1

        # Update prompt coverage
        if prompt_id and validate_answer(answer_text):
            self._covered_prompts.add(prompt_id)

        # Update embedding history
        self._recent_embeddings.append(answer_embedding)
        self._past_embeddings.append(answer_embedding)

        # Keep recent window bounded (last 50 for saturation check)
        if len(self._recent_embeddings) > 50:
            self._recent_embeddings.pop(0)

        # Compute metrics
        coverage = self._compute_coverage()
        saturation = self._compute_saturation(answer_embedding)

        metrics = {
            "iteration": self._iteration,
            "phase": self._phase_config.display_name,
            "coverage_ratio": round(coverage, 4),
            "saturation_score": round(saturation, 4),
            "covered_prompts": len(self._covered_prompts),
            "total_prompts": self._total_prompts,
            "is_complete": self._check_complete(coverage, saturation),
        }
        self._metrics_log.append(metrics)

        log_event(
            "core.training",
            f"[PhaseMonitor] phase={self._phase_config.display_name} | "
            f"iter={self._iteration} | "
            f"coverage={coverage:.3f} | "
            f"saturation={saturation:.3f} | "
            f"complete={metrics['is_complete']}",
            level="debug"
        )
        return metrics

    def is_complete(self) -> bool:
        """
        Returns True if both coverage and saturation thresholds
        are met, or max_iterations is reached.
        """
        if self._iteration >= self._thresholds["max_iterations"]:
            logger.warning(
                f"Phase {self._phase_config.display_name} reached "
                f"max_iterations={self._thresholds['max_iterations']} "
                "without meeting thresholds. Forcing advancement."
            )
            return True
        coverage = self._compute_coverage()
        saturation = self._compute_saturation(
            self._past_embeddings[-1]
            if self._past_embeddings
            else np.zeros(768)
        )
        return self._check_complete(coverage, saturation)

    def get_metrics(self) -> Dict:
        """
        Return current phase completion metrics snapshot.
        Safe to call at any time.
        """
        coverage = self._compute_coverage()
        saturation = (
            self._compute_saturation(self._past_embeddings[-1])
            if self._past_embeddings
            else 0.0
        )
        return {
            "phase": self._phase_config.display_name,
            "phase_id": self._phase_config.id,
            "iteration": self._iteration,
            "coverage_ratio": round(coverage, 4),
            "coverage_target": self._thresholds["coverage_ratio"],
            "saturation_score": round(saturation, 4),
            "saturation_target": self._thresholds["saturation_score"],
            "covered_prompts": len(self._covered_prompts),
            "total_prompts": self._total_prompts,
            "is_complete": self.is_complete(),
            "thresholds": self._thresholds,
        }

    def get_metrics_log(self) -> List[Dict]:
        """Return full per-iteration metrics history."""
        return self._metrics_log

    def reset(self, phase_config: PhaseConfig, phase_prompts: List[Dict]):
        """
        Reset monitor for a new phase.
        Call immediately after phase advancement.
        """
        self._phase_config = phase_config
        self._phase_prompts = phase_prompts
        self._total_prompts = max(len(phase_prompts), 1)
        self._covered_prompts = set()
        self._recent_embeddings = []
        self._past_embeddings = []
        self._iteration = 0
        self._metrics_log = []
        logger.info(
            f"PhaseCompletionMonitor reset for phase: "
            f"{phase_config.display_name}"
        )

    # ─── Internal computation ─────────────────────────────────────────────

    def _compute_coverage(self) -> float:
        """Coverage ratio: fraction of prompts with valid answers."""
        return len(self._covered_prompts) / self._total_prompts

    def _compute_saturation(self, new_embedding: np.ndarray) -> float:
        """
        Semantic saturation: average similarity of new_embedding
        to last top_k embeddings in history.
        Delegates to canonical compute_saturation in utils/helpers.py.
        """
        if len(self._past_embeddings) < 2:
            return 0.0
        # Exclude the embedding just appended (itself)
        history = self._past_embeddings[:-1]
        return compute_saturation(new_embedding, history, top_k=5)

    def _check_complete(self, coverage: float, saturation: float) -> bool:
        """Returns True if both thresholds simultaneously met."""
        return (
            coverage >= self._thresholds["coverage_ratio"]
            and saturation >= self._thresholds["saturation_score"]
        )


# ─── Training loop integration helper ────────────────────────────────────────

def build_phase_monitor(
    phase_id: int,
    phase_prompts: Optional[List[Dict]] = None,
    thresholds: Optional[Dict] = None,
) -> PhaseCompletionMonitor:
    """
    Convenience factory: build a PhaseCompletionMonitor for a given
    phase ID.

    Args:
        phase_id:      1-indexed phase ID (matches PHASES list).
        phase_prompts: List of prompt dicts with 'id' field.
                       Pass empty list if prompts not yet available —
                       coverage tracking will be disabled but saturation
                       still works.
        thresholds:    Optional threshold overrides.

    Returns:
        PhaseCompletionMonitor ready for use in training loop.

    Usage in training loop:
        monitor = build_phase_monitor(7, phase_prompts)
        for answer in student_outputs:
            emb = embedder.embed_text(answer.text)
            metrics = monitor.update(answer.text, emb, answer.prompt_id)
            log_event("training", str(metrics))
            if monitor.is_complete():
                curriculum.advance_phase()
                monitor.reset(
                    get_phase(curriculum.current_phase + 1),
                    next_phase_prompts
                )
    """
    phase_config = get_phase(phase_id)
    if phase_config is None:
        raise ValueError(f"Unknown phase_id={phase_id}")
    prompts = phase_prompts or []
    return PhaseCompletionMonitor(phase_config, prompts, thresholds)
