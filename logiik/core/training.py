@dataclass
class TeacherQuestion:
    """
    A single Phase 7 teacher-generated question with stepwise reasoning.
    Stored in Firebase via TextStore.
    """
    question_id: str
    prompt: str
    answer_steps: List[str]
    full_answer: str
    phase_id: int = 7
    difficulty: float = 0.94


@dataclass
class StudentAttempt:
    """
    A single student attempt at a Phase 7 question.
    May be updated through multiple feedback iterations.
    """
    question_id: str
    attempt_index: int
    student_answer_steps: List[str]
    student_full_answer: str
    feedback: Optional[List[str]] = None
    correctness: Optional[float] = None
    suggested_improvement: Optional[str] = None


# ─── Phase 7 Teacher-Student Loop ────────────────────────────────────────────

class Phase7TeacherStudentLoop:
    """
    Iterative teacher-student training loop for Phase 7:
    Niche Scientific Reasoning.

    Workflow per question:
      1. Teacher generates question + stepwise reasoning.
      2. Student generates stepwise answer + full answer.
      3. Teacher evaluates: assigns correctness score + feedback.
      4. If correctness < threshold (0.90): student regenerates.
      5. Loop until correctness >= 0.90 or max_iterations reached.
      6. Store final QA pair in Firebase + Pinecone.

    All questions and attempts persisted to Firebase via TextStore.
    """

    CORRECTNESS_THRESHOLD = 0.90
    MAX_ITERATIONS = 10    # Max feedback loops per question

    def __init__(self, text_store: Optional[TextStore] = None):
        self._store = text_store or TextStore()
        # In-memory stores (mirrored to Firebase)
        self._teacher_db: Dict[str, TeacherQuestion] = {}
        self._student_db: Dict[str, List[StudentAttempt]] = {}
        self._metrics: Dict[str, Dict] = {}
        logger.info("Phase7TeacherStudentLoop initialised.")

    def generate_teacher_question(
        self,
        prompt: str,
        answer_steps: List[str],
        full_answer: str,
        difficulty: float = 0.94
    ) -> str:
        """
        Register a teacher-generated question.

        In production, prompt + answer_steps are generated by the
        teacher model (GPT-4 / Claude API call). This method stores
        the result and returns the question_id.

        Args:
            prompt:       The scientific question/scenario.
            answer_steps: List of stepwise reasoning strings.
            full_answer:  Complete natural language answer.
            difficulty:   Difficulty score (0–1).

        Returns:
            question_id: UUID string.
        """
        qid = str(uuid.uuid4())
        question = TeacherQuestion(
            question_id=qid,
            prompt=prompt,
            answer_steps=answer_steps,
            full_answer=full_answer,
            difficulty=difficulty,
        )
        self._teacher_db[qid] = question
        self._student_db[qid] = []

        # Persist to Firebase
        self._store.store_phase7_teacher(qid, {
            "prompt": prompt,
            "answer_steps": answer_steps,
            "full_answer": full_answer,
            "difficulty": difficulty,
            "phase_id": 7,
        })

        logger.debug(f"Teacher question registered: id={qid}")
        return qid

    def student_attempt(
        self,
        question_id: str,
        student_answer_steps: List[str],
        student_full_answer: str
    ) -> StudentAttempt:
        """
        Record a student attempt at a Phase 7 question.

        Args:
            question_id:          ID from generate_teacher_question.
            student_answer_steps: Student's stepwise reasoning.
            student_full_answer:  Student's complete answer.

        Returns:
            StudentAttempt object (correctness not yet assigned).
        """
        if question_id not in self._student_db:
            self._student_db[question_id] = []

        attempt_index = len(self._student_db[question_id])
        attempt = StudentAttempt(
            question_id=question_id,
            attempt_index=attempt_index,
            student_answer_steps=student_answer_steps,
            student_full_answer=student_full_answer,
        )
        self._student_db[question_id].append(attempt)

        logger.debug(
            f"Student attempt recorded: "
            f"question_id={question_id}, attempt={attempt_index}"
        )
        return attempt

    def provide_feedback(
        self,
        question_id: str,
        attempt_index: int,
        feedback: List[str],
        correctness: float,
        suggested_improvement: Optional[str] = None
    ) -> bool:
        """
        Apply teacher feedback to a student attempt.

        Args:
            question_id:            Target question ID.
            attempt_index:          Index of the attempt to update.
            feedback:               List of feedback strings per step.
            correctness:            Score 0–1 (threshold: 0.90).
            suggested_improvement:  Optional specific improvement hint.

        Returns:
            True if correctness threshold met (phase 7 QA accepted).
            False if student should regenerate.
        """
        if question_id not in self._student_db:
            logger.error(f"provide_feedback: unknown question_id={question_id}")
            return False

        attempts = self._student_db[question_id]
        if attempt_index >= len(attempts):
            logger.error(
                f"provide_feedback: attempt_index={attempt_index} "
                f"out of range for question_id={question_id}"
            )
            return False

        attempt = attempts[attempt_index]
        attempt.feedback = feedback
        attempt.correctness = correctness
        attempt.suggested_improvement = suggested_improvement

        # Persist feedback to Firebase
        self._store.store_phase7_student(
            f"{question_id}_attempt_{attempt_index}",
            {
                "question_id": question_id,
                "attempt_index": attempt_index,
                "student_answer_steps": attempt.student_answer_steps,
                "student_full_answer": attempt.student_full_answer,
                "feedback": feedback,
                "correctness": correctness,
                "suggested_improvement": suggested_improvement or "",
            }
        )

        threshold_met = correctness >= self.CORRECTNESS_THRESHOLD
        status = "ACCEPTED" if threshold_met else "NEEDS_REVISION"
        logger.info(
            f"Feedback applied: question_id={question_id}, "
            f"attempt={attempt_index}, correctness={correctness:.2f}, "
            f"status={status}"
        )
        return threshold_met

    def run_feedback_loop(
        self,
        question_id: str,
        student_fn,
        teacher_fn
    ) -> Tuple[bool, int, float]:
        """
        Run the full iterative feedback loop for one question.

        Args:
            question_id: Target question ID.
            student_fn:  Callable(prompt, steps, suggestion) ->
                         (answer_steps: List[str], full_answer: str)
            teacher_fn:  Callable(prompt, teacher_steps,
                         student_steps, student_answer) ->
                         (feedback: List[str], correctness: float,
                          suggestion: str)

        Returns:
            Tuple of (threshold_met: bool, iterations: int,
                      final_correctness: float)
        """
        if question_id not in self._teacher_db:
            logger.error(
                f"run_feedback_loop: unknown question_id={question_id}"
            )
            return False, 0, 0.0

        question = self._teacher_db[question_id]
        threshold_met = False
        final_correctness = 0.0
        suggestion = None

        for iteration in range(self.MAX_ITERATIONS):
            # Student generates answer
            student_steps, student_answer = student_fn(
                question.prompt,
                question.answer_steps,
                suggestion
            )

            # Validate minimum answer quality
            if not validate_answer(student_answer):
                logger.warning(
                    f"Student answer failed validation at iteration "
                    f"{iteration} for question_id={question_id}"
                )
                continue

            # Record attempt
            self.student_attempt(question_id, student_steps, student_answer)

            # Teacher evaluates
            feedback, correctness, suggestion = teacher_fn(
                question.prompt,
                question.answer_steps,
                student_steps,
                student_answer
            )

            # Apply feedback
            threshold_met = self.provide_feedback(
                question_id,
                iteration,
                feedback,
                correctness,
                suggestion
            )
            final_correctness = correctness

            if threshold_met:
                logger.info(
                    f"Phase 7 question accepted after {iteration + 1} "
                    f"iterations: correctness={correctness:.2f}"
                )
                break
            else:
                logger.debug(
                    f"Iteration {iteration + 1}: correctness={correctness:.2f} "
                    f"< {self.CORRECTNESS_THRESHOLD} — regenerating."
                )

        if not threshold_met:
            logger.warning(
                f"Phase 7 question {question_id} did not meet threshold "
                f"after {self.MAX_ITERATIONS} iterations. "
                f"Final correctness={final_correctness:.2f}"
            )

        # Record metrics
        self._metrics[question_id] = {
            "iterations": min(self.MAX_ITERATIONS, iteration + 1),
            "final_correctness": final_correctness,
            "threshold_met": threshold_met,
        }

        return threshold_met, iteration + 1, final_correctness

    def get_metrics(self) -> Dict:
        """Return aggregate Phase 7 loop metrics."""
        if not self._metrics:
            return {"total_questions": 0}
        total = len(self._metrics)
        accepted = sum(1 for m in self._metrics.values() if m["threshold_met"])
        avg_iter = np.mean([m["iterations"] for m in self._metrics.values()])
        avg_corr = np.mean(
            [m["final_correctness"] for m in self._metrics.values()]
        )
        return {
            "total_questions": total,
            "accepted": accepted,
            "acceptance_rate": accepted / total,
            "avg_iterations": float(avg_iter),
            "avg_final_correctness": float(avg_corr),
        }


# ─── Processed example for DataLoader ────────────────────────────────────────

@dataclass
class ProcessedExample:
    """Tokenized training example for DataLoader."""
    input_ids: torch.Tensor
    attention_mask: torch.Tensor
    labels: torch.Tensor
    teacher_logits: Optional[torch.Tensor]
    weight: float


def collate_examples(batch: List[ProcessedExample]) -> ProcessedExample:
    """
    Custom collate — stacks fixed-size tensors.
    Drops variable-length teacher_logits to avoid shape mismatch.
    Preserved from legacy cognita/training/curriculum.py.
    """
    return ProcessedExample(
        input_ids=torch.stack([b.input_ids for b in batch]),
        attention_mask=torch.stack([b.attention_mask for b in batch]),
        labels=torch.stack([b.labels for b in batch]),
        teacher_logits=None,
        weight=sum(b.weight for b in batch) / len(batch),
    )


